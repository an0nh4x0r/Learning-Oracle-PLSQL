      WHEN v_val = 10 THEN DBMS_OUTPUT.PUT_LINE('ITS 10');
      WHEN v_val = 11 THEN DBMS_OUTPUT.PUT_LINE('ITS 11');
      WHEN v_val = 12 THEN DBMS_OUTPUT.PUT_LINE('ITS 12');
      ELSE DBMS_OUTPUT.PUT_LINE('WHAT IS IT THEN?');
    END CASE;
  END;

