    DBMS_OUTPUT.PUT_LINE('TRUE');
  END IF;

  DBMS_OUTPUT.PUT_LINE('IF THEN ELSE END IF');
  IF FALSE THEN
    DBMS_OUTPUT.PUT_LINE('TRUE');
  ELSE
    DBMS_OUTPUT.PUT_LINE('FALSE');
  END IF;

  DBMS_OUTPUT.PUT_LINE('IF THEN ELSIF ELSE END IF');
  IF 3 < 3 THEN
    DBMS_OUTPUT.PUT_LINE('NOPE ... THATS NOT TRUE');
