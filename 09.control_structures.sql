    DBMS_OUTPUT.PUT_LINE('NOW ... THATS TRUE');
  ELSE
    DBMS_OUTPUT.PUT_LINE('THIS DEFINATELY NEVER GONNA HAPPEN');
  END IF;

  DECLARE
    v_val NUMBER := 130;
